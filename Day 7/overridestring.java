class Student {
    int id;
    String name;

    public Student(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public String toString() {
        return "ID: " + id + ", Name: " + name;
    }
}
public class overridetostring {
    public static void main(String[] args) {
        Student s = new Student(101, "Jamuna");
        System.out.println(s);
    }
}
